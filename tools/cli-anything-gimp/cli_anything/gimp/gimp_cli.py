@click.group(invoke_without_command=True)
@click.option("--json", "use_json", is_flag=True, help="Output in JSON format for agent consumption.")
@click.option("--project", "-p", "project_path", help="Auto-load project file.")
@click.version_option("1.0.0", prog_name="cli-anything-gimp")
@click.pass_context
def main(ctx, use_json, project_path):
    """GIMP CLI - Raster image processing via CLI.

    A stateful CLI for image editing with layers, filters, canvas
    manipulation, and export. Uses Pillow as primary engine with
    optional GIMP batch mode fallback.
    """
    global _json_output
    _json_output = use_json

    if project_path:
        result = proj_mod.open_project(project_path)
        if result["ok"]:
            _session.project = result["project"]
            _session.project_path = project_path

    if ctx.invoked_subcommand is None:
        _start_repl()


# ─── REPL ─────────────────────────────────────────────────────────────────

def _start_repl():
    """Start interactive REPL mode."""
    global _repl_mode
    _repl_mode = True
    skin = ReplSkin()
    skin.print_banner()

    repl_commands = {
        "help": "Show this help",
        "quit/exit": "Exit REPL",
        "undo": "Undo last action",
        "redo": "Redo last undone action",
        "history": "Show undo history",
        "save": "Save current project",
        "info": "Show project info",
        "layers": "List layers",
        "filters": "List applied filters",
        "canvas": "Show canvas info",
    }

    while True:
        try:
            line = skin.get_input()
            if line is None:
                break
            line = line.strip()
            if not line:
                continue
            if line in ("quit", "exit", "q"):
                skin.info("Session ended.")
                break
            elif line == "help":
                skin.help(repl_commands)
            elif line == "undo":
                result = _session.undo()
                output(result)
            elif line == "redo":
                result = _session.redo()
                output(result)
            elif line == "history":
                output({"ok": True, "history": _session.list_history()})
            elif line == "save":
                result = _session.save_session()
                output(result)
            elif line == "info":
                if _session.project:
                    output(proj_mod.get_project_info(_session.project))
                else:
                    skin.warning("No project loaded.")
            elif line == "layers":
                if _session.project:
                    output(layer_mod.list_layers(_session.project))
                else:
                    skin.warning("No project loaded.")
            elif line == "filters":
                if _session.project:
                    output(filter_mod.list_filters(_session.project))
                else:
                    skin.warning("No project loaded.")
            elif line == "canvas":
                if _session.project:
                    output(canvas_mod.get_canvas_info(_session.project))
                else:
                    skin.warning("No project loaded.")
            else:
                # Try to parse as a CLI subcommand
                try:
                    args = line.split()
                    main(args, standalone_mode=False)
                except SystemExit:
                    pass
                except click.exceptions.UsageError as e:
                    skin.error(str(e))
        except KeyboardInterrupt:
            print()
            continue


