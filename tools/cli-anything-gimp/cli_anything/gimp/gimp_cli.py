@main.group("filter")
def filter_group():
    """Apply, remove, and manage image filters."""
    pass


@filter_group.command("add")
@click.argument("filter_name")
@click.option("--layer", "layer_id", help="Apply to specific layer (default: canvas).")
@click.option("--param", "-p", multiple=True, help="Filter params as key=value.")
@handle_error
@require_project
def filter_add(filter_name, layer_id, param):
    """Add a filter to the project or a layer."""
    _session.snapshot(f"Before add filter: {filter_name}")
    params = {}
    for p in param:
        if "=" in p:
            k, v = p.split("=", 1)
            try:
                v = float(v)
                if v == int(v):
                    v = int(v)
            except ValueError:
                pass
            params[k] = v
    target = "layer" if layer_id else "canvas"
    result = filter_mod.add_filter(_session.project, filter_name, target, layer_id, **params)
    output(result)


@filter_group.command("remove")
@click.argument("filter_id")
@click.option("--layer", "layer_id", help="Remove from specific layer.")
@handle_error
@require_project
def filter_remove(filter_id, layer_id):
    """Remove a filter by ID."""
    _session.snapshot(f"Before remove filter: {filter_id}")
    result = filter_mod.remove_filter(_session.project, filter_id, layer_id)
    output(result)


@filter_group.command("set")
@click.argument("filter_id")
@click.argument("param")
@click.argument("value")
@click.option("--layer", "layer_id")
@handle_error
@require_project
def filter_set(filter_id, param, value, layer_id):
    """Update a filter parameter."""
    try:
        value = float(value)
        if value == int(value):
            value = int(value)
    except ValueError:
        pass
    _session.snapshot(f"Before set filter param: {filter_id}.{param}")
    result = filter_mod.set_filter_param(_session.project, filter_id, param, value, layer_id)
    output(result)


@filter_group.command("list")
@click.option("--category", "-c", help="Filter by category.")
@click.option("--available", is_flag=True, help="Show all available filters.")
@handle_error
def filter_list(category, available):
    """List applied or available filters."""
    if available:
        result = filter_mod.list_filters(category=category)
    else:
        if _session.project is None:
            output({"ok": False, "error": "No project loaded."})
            return
        result = filter_mod.list_filters(_session.project, category)
    output(result)


