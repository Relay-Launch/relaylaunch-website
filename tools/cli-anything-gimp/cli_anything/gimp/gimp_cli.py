def output(data):
    """Print output as JSON or human-readable."""
    if _json_output:
        click.echo(json.dumps(data, indent=2, default=str))
    else:
        if isinstance(data, dict):
            if not data.get("ok", True):
                click.secho(f"Error: {data.get('error', 'Unknown error')}", fg="red")
                return
            for key, val in data.items():
                if key == "ok":
                    continue
                if isinstance(val, (dict, list)):
                    click.echo(f"  {key}: {json.dumps(val, indent=2, default=str)}")
                else:
                    click.echo(f"  {key}: {val}")
        else:
            click.echo(data)


def handle_error(f):
    """Decorator for consistent error handling."""
    @functools.wraps(f)
    def wrapper(*args, **kwargs):
        try:
            return f(*args, **kwargs)
        except FileNotFoundError as e:
            output({"ok": False, "error": f"File not found: {e}"})
        except json.JSONDecodeError as e:
            output({"ok": False, "error": f"Invalid JSON: {e}"})
        except Exception as e:
            output({"ok": False, "error": str(e)})
    return wrapper


def require_project(f):
    """Decorator that ensures a project is loaded."""
    @functools.wraps(f)
    def wrapper(*args, **kwargs):
        if _session.project is None:
            output({"ok": False, "error": "No project loaded. Use 'project new' or 'project open' first."})
            return
        return f(*args, **kwargs)
    return wrapper


